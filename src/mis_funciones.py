import pandas as pd
import numpy as np

def suma(a,b):

    """
    Recibe dos float y devuelve la suma
    """
    result = a+b
    
    return result


def resta(a,b):
    """recibe dos float y resta el segundo al primero

    Args:
        a (float): cualquier numero decimal, positivo o negativo
        b (float): cualquier numero decimal, positivo o negativo
    """
    return (a-b)
